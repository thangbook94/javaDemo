package java8;

public class AnotherPerson {
    private double score;
    private int age;
    private String gender;

    public AnotherPerson(double score, int age, String gender) {
        this.score = score;
        this.age = age;
        this.gender = gender;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
}

